public class OperatorPrecedence{
    public static void main(String[] args){
        System.out.println("10+5*2 =" + (10+5*2));
        System.out.println("(10+5)*2 =" +((10+5)*2));
        System.out.println("20-5+3 =" + (20-5+3));
        System.out.println("100/5*2 =" + (100/5*2));

    }

}
